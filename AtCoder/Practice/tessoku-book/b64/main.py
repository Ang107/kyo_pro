# ed=隣接リスト[(next, weight)], 初期ノード
def dijkstra(ed, st):
    # 初期化
    n = len(ed)
    distance = [inf] * n
    distance[st] = 0
    heap = [(0, st)]
    frm = [-1] * n
    frm[0] = "s"
    # ダイクストラ
    while heap:
        dis, v = heappop(heap)
        if distance[v] < dis:
            continue
        for next, weight in ed[v]:
            new_distance = distance[v] + weight
            if new_distance < distance[next]:
                frm[next] = v
                distance[next] = new_distance
                heappush(heap, (new_distance, next))
    return distance, frm


